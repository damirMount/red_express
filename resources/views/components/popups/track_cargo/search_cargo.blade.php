                    $('#status').text(data.status)
                    $('#country').text(data.country)
                    $('#time').text(data.time)

                    $('#invoice').val('');
                    $('#modalSearch').modal('toggle');
                    $('#modalResult').modal('show');
                }
                else {
                    $('#empty-cargo').text(data.status);
                    $('#empty-cargo').removeClass('d-none');
